function ensureAudioCtx() {
  if (!audioCtx) {
    audioCtx = new (window.AudioContext || window.webkitAudioContext)();
  }
  if (audioCtx.state === 'suspended') {
    audioCtx.resume();
  }
}

const trackGenerators = {
  ocean: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const size = ctx.sampleRate * 2;
    const buffer = ctx.createBuffer(1, size, ctx.sampleRate);
    const data = buffer.getChannelData(0);
    for (let i = 0; i < size; i++) data[i] = (Math.random() * 2 - 1) * 0.6;
    const src = ctx.createBufferSource();
    src.buffer = buffer;
    src.loop = true;
    const lp = ctx.createBiquadFilter();
    lp.type = 'lowpass';
    lp.frequency.value = 800;
    const gain = ctx.createGain();
    gain.gain.value = vol;
    src.connect(lp).connect(gain).connect(ctx.destination);
    src.start();
    return { stop: () => src.stop(), gain };
  },
  rain: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const size = ctx.sampleRate;
    const buffer = ctx.createBuffer(1, size, ctx.sampleRate);
    const data = buffer.getChannelData(0);
    for (let i = 0; i < size; i++) data[i] = (Math.random() * 2 - 1) * 0.8;
    const src = ctx.createBufferSource();
    src.buffer = buffer;
    src.loop = true;
    const hp = ctx.createBiquadFilter();
    hp.type = 'highpass';
    hp.frequency.value = 400;
    const gain = ctx.createGain();
    gain.gain.value = vol;
    src.connect(hp).connect(gain).connect(ctx.destination);
    src.start();
    return { stop: () => src.stop(), gain };
  },
  forest: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const size = ctx.sampleRate * 2;
    const buffer = ctx.createBuffer(1, size, ctx.sampleRate);
    const data = buffer.getChannelData(0);
    for (let i = 0; i < size; i++) data[i] = (Math.random() * 2 - 1) * 0.4;
    const src = ctx.createBufferSource();
    src.buffer = buffer;
    src.loop = true;
    const lp = ctx.createBiquadFilter();
    lp.type = 'lowpass';
    lp.frequency.value = 1200;
    const gain = ctx.createGain();
    gain.gain.value = vol;
    src.connect(lp).connect(gain).connect(ctx.destination);
    src.start();
    return { stop: () => src.stop(), gain };
  },
  bells: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const osc = ctx.createOscillator();
    osc.type = 'sine';
    osc.frequency.value = 440;
    const gain = ctx.createGain();
    gain.gain.value = vol * 0.5;
    const delay = ctx.createDelay();
    delay.delayTime.value = 1;
    const fb = ctx.createGain();
    fb.gain.value = 0.4;
    osc.connect(gain).connect(delay).connect(ctx.destination);
    delay.connect(fb).connect(delay);
    osc.start();
    return { stop: () => osc.stop(), gain };
  },
  flute: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const osc = ctx.createOscillator();
    osc.type = 'triangle';
    osc.frequency.value = 520;
    const vib = ctx.createOscillator();
    vib.frequency.value = 5;
    const vibGain = ctx.createGain();
    vibGain.gain.value = 10;
    vib.connect(vibGain).connect(osc.frequency);
    const gain = ctx.createGain();
    gain.gain.value = vol * 0.4;
    osc.connect(gain).connect(ctx.destination);
    osc.start();
    vib.start();
    return { stop: () => { osc.stop(); vib.stop(); }, gain };
  },
  choir: (vol) => {
    ensureAudioCtx();
    const ctx = audioCtx;
    const oscs = [];
    const merger = ctx.createGain();
    for (let i = 0; i < 4; i++) {
      const osc = ctx.createOscillator();
      osc.type = 'sine';
      osc.frequency.value = 220 + i * 7;
      osc.connect(merger);
      osc.start();
      oscs.push(osc);
    }
    const gain = ctx.createGain();
    gain.gain.value = vol * 0.3;
    merger.connect(gain).connect(ctx.destination);
    return { stop: () => oscs.forEach(o => o.stop()), gain };
  }
};

musicTracks.forEach(track => {
  track.addEventListener('click', () => {
    // Stop current track
    if (currentTrack) {
      currentTrack.stop();
      currentTrack = null;
    }
    
    // Remove active class from all
    musicTracks.forEach(t => t.classList.remove('active'));
    
    const trackType = track.dataset.track;
    const volume = musicVolume.value / 100;
    
    if (trackGenerators[trackType]) {
      currentTrack = trackGenerators[trackType](volume);
      track.classList.add('active');
    }
  });
});

musicPlayPause.addEventListener('click', () => {
  if (audioCtx) {
    if (audioCtx.state === 'running') {
      audioCtx.suspend();
      musicPlayPause.textContent = '▶️';
    } else {
      audioCtx.resume();
      musicPlayPause.textContent = '⏸️';
    }
  }
});

musicStop.addEventListener('click', () => {
  if (currentTrack) {
    currentTrack.stop();
    currentTrack = null;
    musicTracks.forEach(t => t.classList.remove('active'));
  }
});

