favoriteBtn.addEventListener('click', () => {
  const index = favorites.indexOf(currentPoemId);
  if (index > -1) {
    favorites.splice(index, 1);
  } else {
    favorites.push(currentPoemId);
  }
  localStorage.setItem('favorites', JSON.stringify(favorites));
  updateFavoriteButton();
  updateStatsUI();
});

// ===== TEXT-TO-SPEECH =====
speakerBtn.addEventListener('click', () => {
  if (currentSpeech) {
    speechSynthesis.cancel();
    currentSpeech = null;
    speakerBtn.querySelector('.action-icon').textContent = '🔊';
    speakerBtn.classList.remove('active');
    return;
  }

  const poem = poemsData[currentPoemId];
  if (!poem) return;

  const utterance = new SpeechSynthesisUtterance(poem.content);
  utterance.rate = 0.9;
  utterance.pitch = 1;
  
  utterance.onend = () => {
    currentSpeech = null;
    speakerBtn.querySelector('.action-icon').textContent = '🔊';
    speakerBtn.classList.remove('active');
  };

  currentSpeech = utterance;
  speakerBtn.querySelector('.action-icon').textContent = '⏹️';
  speakerBtn.classList.add('active');
  speechSynthesis.speak(utterance);
});

